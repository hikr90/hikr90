package ex1_class_array;

public class ArrayTest {
	
	// 클래스 안에서 Getter, Setter 생성
	private String name;
	private int age;

	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
}
