package com.lnsp.vo;

public class ChangeVO {
	
	private int cha_idx;
	private int ol_num;
	private int m_num;
	private String cha_reason;
	private String cha_return;
	
	public int getCha_idx() {
		return cha_idx;
	}
	public void setCha_idx(int cha_idx) {
		this.cha_idx = cha_idx;
	}
	public int getOl_num() {
		return ol_num;
	}
	public void setOl_num(int ol_num) {
		this.ol_num = ol_num;
	}
	public int getM_num() {
		return m_num;
	}
	public void setM_num(int m_num) {
		this.m_num = m_num;
	}
	public String getCha_reason() {
		return cha_reason;
	}
	public void setCha_reason(String cha_reason) {
		this.cha_reason = cha_reason;
	}
	public String getCha_return() {
		return cha_return;
	}
	public void setCha_return(String cha_return) {
		this.cha_return = cha_return;
	}
	
	

}
